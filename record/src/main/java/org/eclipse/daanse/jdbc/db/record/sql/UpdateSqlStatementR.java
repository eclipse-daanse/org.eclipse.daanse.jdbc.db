package org.eclipse.daanse.jdbc.db.record.sql;

import java.util.List;
import java.util.Optional;

import org.eclipse.daanse.jdbc.db.api.schema.TableReference;
import org.eclipse.daanse.jdbc.db.api.sql.UpdateSqlStatement;

public record UpdateSqlStatementR(
        TableReference table,
        List<ColumnValue> setClause,
        Optional<String> whereClause) implements UpdateSqlStatement {

    /**
     * Creates an UpdateSqlStatement without WHERE clause (updates all rows)
     */
    public UpdateSqlStatementR(TableReference table, List<ColumnValue> setClause) {
        this(table, setClause, Optional.empty());
    }
}
